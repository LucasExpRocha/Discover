// Operadores de comparação

// Irá comparar valores e retornar um boolean como resposta á comparação

let one = 1
let two = 2

// == igual a

console.log(one == 1)
console.log(two == 1)
console.log(one == '1')
console.log(two == '2')

// !- diferente de

// === estritamente igual a
console.log(one === '1')
console.log(one === 1)

// !== estritamente diferente de
console.log(two !== "2")
console.log(two !== 2)

// > Maior que

console.log(one > two)

// >= Maior igual a

console.log(one >= 1)
console.log(one >= 1)

// < menor que

console.log(one < two)

// <= Menor igual a

console.log(one <= two)
console.log(one <= 1)
console.log(one <= 0)